$CRUCLASSON = 0;

function SetClass(%id,%class)
{
	%id.Class = $CLASS[%class];
	Game::RefreshClientScore(%id);
}

function CreateClassTome(%req,%stats,%names)
{
	for (%i = 0; %i <= 9; %i++) {
		$CRUCLASSON++;
		%n = getWord(%names,%i);
		%s = "";
		for (%x = 0; (%g = GetWord(%stats,%x)) != -1; %x+=2) {
			%v = getWord(%stats,%x+1);
			%s = %s @ %g @ " H:" @ round(%v * ((%i + 1) / 10)) @ " ";
		}
		%s = %s @ "SETCLASS H:" @ $CRUCLASSON;
		%on = $CRUCLASSON;
		$CLASS[%on] = %n;
		if (%req == "LITSTR") %ico = "ico_tome_str.bmp";
		if (%req == "LITDEX") %ico = "ico_tome_dex.bmp";
		if (%req == "LITINT") %ico = "ico_tome_int.bmp";
		if (%req == "LITMAX") %ico = "ico_scroll.bmp";
		CreateUniqueItem(%n @ " Tome","Study","NON 0 0",%req,%s,%ico,%n @ " Tome","",1,1);
	}
}

