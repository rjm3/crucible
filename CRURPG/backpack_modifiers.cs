function AddRune(%rune,%mod,%v,%type)
{
	echo("*** ADD RUNE:" @ %rune @ " MOD:" @ %mod @ " V:" @ %v @ " TYPE:" @ %type);

	if ((%runename = $BPRUNENAME[%type]) != "") {
		%vtname = %runename @ "RuneOf" @ %rune;
		%vname = %runename @ " Rune of " @ %rune;
	}
	else {
		%vtname = %type @ "RuneOf" @ %rune;
		%vname = %type @ " Rune of " @ %rune;
	}

	//echo("*** VTNAME " @ %vtname @ " VNAME " @ %vname);

	//AddToTestLoot(%vtname);

	%req = $TierReq[%mod];

	AddToRuneLoot(%vtname,%type);
	
	if (%req == $ReqStr) %ico = "ico_rune_str.bmp";
	if (%req == $ReqDex) %ico = "ico_rune_dex.bmp";
	if (%req == $ReqInt) %ico = "ico_rune_int.bmp";

	//echo(" *** REQ " @ %req @ " ICO" @ %ico);

	$DynamicItem[%vtname,$DName] = %vname;
	$DynamicItem[%vtname,$DMinMax] = "1 300";
	$DynamicItem[%vtname,$DWeight] = "1 0";
	$DynamicItem[%vtname,$DPrice] = "3000 0";
	$DynamicItem[%vtname,$DDesc] = %desc;
	$DynamicItem[%vtname,$DItemType] = "1 Rune";
	$DynamicItem[%vtname,$DIco] = %ico;
	$DynamicItem[%vtname,$DRune] = 1;
	$DynamicItem[%vtname,$DRuneReq] = %req;
	$DynamicItem[%vtname,$DRuneBonus] = %mod @ " " @ %v;
	$DynamicItem[%vtname,$DRuneLoc] = %type;
}

function CreateTierList(%wear,%type)
{
	echo("[CREATETIERLIST] " @ %wear @ " " @ %type);
	$BPTIERLST[%wear,%type] = "";
	%list = "";
	for (%i = 1; (%g = $BPTIERVAR[%wear,%type,%i]) != ""; %i++)
		%list = %list @ %g @ " ";
	$BPTIERLST[%wear,%type] = %list;
}

